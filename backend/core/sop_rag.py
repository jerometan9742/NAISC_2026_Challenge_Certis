SOP_GUIDELINES = """
=== CERTIS SECURITY RESPONSE SOPs ===

PHYSICAL ALTERCATION / FIGHT
- Dispatch minimum 2 officers immediately
- Do NOT engage alone if weapons are involved — call police (999) first
- Seal off area, redirect bystanders
- Render first aid if injuries present
- Activate body-worn camera; preserve CCTV footage
- Complete incident report within 1 hour

FORCED ENTRY / UNAUTHORIZED ACCESS
- Dispatch officer to location immediately
- Lockdown affected access zone
- Call police (999) if intruder is still on-site
- Review access control logs; revoke compromised credentials
- Preserve footage from entry point cameras

UNATTENDED / SUSPICIOUS ITEM
- Immediately clear a 20-metre radius — do NOT touch the item
- Call police (999) and describe item, location, dimensions
- Evacuate surrounding zones; await bomb disposal clearance
- Do not re-enter area until police give all-clear

MEDICAL EMERGENCY / FALLEN PERSON
- Call SCDF ambulance (995) immediately
- Dispatch nearest certified first-aider
- Clear area for emergency responder access
- Keep casualty still; do not move unless in immediate danger
- Notify building management and duty supervisor
- Document time of incident and response

FIRE EMERGENCY
- Activate building evacuation alarm
- Call SCDF (995)
- Deploy floor fire wardens; guide occupants to assembly points
- Seal fire doors; do not use lifts
- Meet SCDF at main entrance; hand over fire panel key

DISTRESS CALL / PANIC BUTTON ACTIVATED
- Dispatch nearest available officer immediately
- Call police (999) if criminal activity is indicated
- Maintain radio contact with responding officer
- Document all details: time, location, nature of call
- Brief supervisor and log in incident system

SUSPICIOUS PERSON / BEHAVIOUR
- Dispatch 1 officer to observe only — do not confront alone
- Request backup before any engagement
- Document: physical description, time, direction of travel, behaviour observed
- Check ID only if there is reasonable cause and a second officer is present
- Report to supervisor; escalate if behaviour continues

CROWD INCIDENT / DISTURBANCE
- Deploy crowd management barriers immediately
- Request additional officers from control room
- Establish clear crowd control zones with physical barriers
- Coordinate with emergency services if crowd exceeds control capacity
- Prevent crush situations near exits; open all available exits
"""
